// using let 
let creativityLevel = 80; // Represents my level of creativity on a scale of 10 to 100

// using var
var patienceLevel = "medium"; // Represents my patience level,can be a string value

// using const
const curiosityLevel = "insatiable"; // Represents my insatiable curiosity

// using let
let empathyLevel = "high"; // Represents my capacity for empathy, can be a string value

// using const
const adaptabilityLevel = 70; // Represents my adaptability level on a scale of 1 to 10

console.log("Creativity Level:", creativityLevel);
